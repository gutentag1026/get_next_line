#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t		i;
	size_t		j;

	i = 0;
	if (!needle[i])
		return (char*)haystack;
	while (haystack[i])
	{
		j = 0;
		while (haystack[j + i] == needle[j] && (i + j) <
			len && needle[j] != '\0')
			j++;
		if (needle[j] == '\0')
			return ((char *)&haystack[i]);
		i++;
	}
	return (NULL);
}
